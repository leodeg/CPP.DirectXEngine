{
	class ErrorLogger
	{
	public:
		// --------------------------------------------------------------------------------
		/// Show error message box
		// --------------------------------------------------------------------------------
		static void Log (std::string message);

		// --------------------------------------------------------------------------------
		/// Show error message box
		// --------------------------------------------------------------------------------
		static void Log (HRESULT hr, std::string message);

		// --------------------------------------------------------------------------------
		/// Show error message box
		// --------------------------------------------------------------------------------
		static void Log (HRESULT hr, std::wstring message);

		// --------------------------------------------------------------------------------
		/// Write message to console
		// --------------------------------------------------------------------------------
		static void LogConsole (std::string message);
		
		// --------------------------------------------------------------------------------
		/// Write message to console
		// --------------------------------------------------------------------------------
		static void LogConsole (HRESULT hr, std::string message);

		// --------------------------------------------------------------------------------
		/// Write message to output debug
		// --------------------------------------------------------------------------------
		static void LogDebug (std::string message);

		// --------------------------------------------------------------------------------
		/// Write message to output debug
		// --------------------------------------------------------------------------------
		static void LogDebug (HRESULT hr, std::string message);
	};
}